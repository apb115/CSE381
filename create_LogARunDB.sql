--============================== Tables
CREATE TABLE Runners (
	runnerID		INT					PRIMARY KEY IDENTITY				NOT NULL,
	runnerName		VARCHAR(30)												NOT NULL,
	address			VARCHAR(30)												NOT NULL,
	city			VARCHAR(30)												NOT NULL,
	state			VARCHAR(20)												NOT NULL,
	zip				VARCHAR(20)												NOT NULL,
	phone			VARCHAR(20)												NOT NULL,
	bestMileTime	FLOAT													NOT NULL,
	encryptedName	VARBINARY(64)											NULL
);
GO

CREATE TABLE Routes (
	routeID			INT					PRIMARY KEY IDENTITY				NOT NULL,
	runnerID		INT,
	routeName		VARCHAR(100)											NOT NULL,
	distance		FLOAT													NOT NULL
);
GO

CREATE TABLE Teams (
	teamID			INT					PRIMARY KEY IDENTITY				NOT NULL,
	teamName		VARCHAR(100)											NOT NULL
);
GO

CREATE TABLE TeamRunners (
	teamID			INT					REFERENCES Teams(teamID)			NOT NULL,
	runnerID		INT					REFERENCES Runners(runnerID)		NOT NULL,
	isTeamOwner		BIT					DEFAULT(0)							NOT NULL,
	joinDate		DATE
	CONSTRAINT PK_TeamRunner			PRIMARY KEY CLUSTERED (
		teamID, 
		runnerID
	)
);
GO

CREATE TABLE Workouts (
	workoutID		INT					PRIMARY KEY IDENTITY				NOT NULL,
	runnerID		INT					REFERENCES Runners(runnerID)		NOT NULL,
	routeID			INT					REFERENCES Routes(routeID)			NULL,
	runDate			DATE													NOT NULL,
	distance		FLOAT													NOT NULL,
	seconds			INT														NOT NULL
);
GO

CREATE TABLE Intervals (
	intervalID		INT					PRIMARY KEY IDENTITY				NOT NULL,
	workoutID		INT					REFERENCES Workouts(workoutID)		NOT NULL,
	distance		FLOAT													NOT NULL,
	seconds			INT														NOT NULL
);
GO

